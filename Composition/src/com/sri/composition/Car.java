//Car is a Vehicle
//It has IS-A relationship
public class Car extends Vehicle{
	private int doors;
	private int engineCapacity;
	
	
	
	public Car(String name, int doors, int engineCapacity) {
		super(name);
		this.doors = doors;
		this.engineCapacity = engineCapacity;
	}
	
	
	
	
	
	
	
	

}
